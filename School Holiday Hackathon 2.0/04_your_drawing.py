import turtle

screen = turtle.Screen()
screen.bgcolor("white")       # Change the background color

t = turtle.Turtle()
t.speed(3)
t.color("black")              # Change the pen color
t.width(2)                    # Change the pen size

# ---- START DRAWING BELOW ----
# Use these commands:
#   t.forward(distance)       — move forward
#   t.backward(distance)      — move backward
#   t.right(angle)            — turn right
#   t.left(angle)             — turn left
#   t.penup()                 — lift pen (don't draw)
#   t.pendown()               — put pen down (draw again)
#   t.color("colorname")      — change color
#   t.circle(radius)          — draw a circle



# ---- END DRAWING ----

turtle.done()
